#include <stdio.h>

int main()
{
    /*
    Assignment Operators.
    1. =
    2. +=
    3. -=
    4. *=
    5. /=
    6. %=
    */

    int a = 11;

    printf("%d\n", a += 5);
    printf("%d\n", a -= 5);
    printf("%d\n", a *= 2);
    printf("%d\n", a /= 11);
    printf("%d\n", a %= 2);
    return 0;
}
